from hydpy.exe.modelimports import *
from hydpy.core import modeltools
from hydpy.core.typingtools import *
from hydpy.models.tm import tm_model


class Model(modeltools.AdHocModel):
    """|tm_2s.DOCNAME.complete|."""

    DOCNAME = modeltools.DocName(
        short="TM-2S", description="teaching model with two storages"
    )
    __HYDPY_ROOTMODEL__ = True

    INLET_METHODS = ()
    OBSERVER_METHODS = ()
    RECEIVER_METHODS = ()
    RUN_METHODS = (
        tm_model.Calc_PNet_V1,
        tm_model.Update_S_V1,
        tm_model.Calc_Perc_QI_S_V1,
        tm_model.Calc_QD_S_V1,
        tm_model.Update_G_V1,
        tm_model.Calc_QB_G_V1,
        tm_model.Calc_QT_V1,
    )
    INTERFACE_METHODS = ()
    ADD_METHODS = ()
    OUTLET_METHODS = (tm_model.Pass_Q_V1,)
    SENDER_METHODS = ()
    SUBMODELINTERFACES = ()
    SUBMODELS = ()

    def check_waterbalance(self, initial_conditions: ConditionsModel) -> float:
        r"""Determine the water balance error of the previous simulation run in mm.

        Method |Model.check_waterbalance| calculates the balance error as follows:

          .. math::
            Error = \Sigma In  - \Sigma Out - \Delta Vol
            \\ \\
            \Sigma In = \sum_{t=t0}^{t1} PNet_t
            \\
            \Sigma Out = \sum_{t=t0}^{t1} QT_t
            \\
            \Delta Vol = (S_{t1} - S_{t0}) + (G_{t1} - G_{t0})
        """
        fluxes = self.sequences.fluxes
        last = self.sequences.states
        first = initial_conditions["model"]["states"]
        return (
            numpy.sum(fluxes.pnet.series)
            - numpy.sum(fluxes.qt.series)
            - (last.s - first["s"])
            - (last.g - first["g"])
        )
